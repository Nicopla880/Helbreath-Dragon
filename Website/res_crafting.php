<? $posttitle = "Magic Gems"; $postid = "Magic_Gems"?>
<? include("post_head.php"); ?>
<p>There are 4 different types of Magic Gem's, with each different effect. As you can see in the table, the following Magic gems do:
</p>
<ul><li> Magic Diamond: Adds hitting probability (HP) to magic and physical attacks when equipped 
</li></ul>
<ul><li> Magic Ruby: Adds health points (HP) regen when equipped
</li></ul>
<ul><li> Magic Emerald: Gives the wearer extra magic absorption (MA) when equipped
</li></ul>
<ul><li> Magic Sapphire: Adds physical absorption (PA) when equipped
</li></ul>
<br />
<p>The purity of your crafted item is determined by the purity of items used to create it. Merien stones are always considered 100%.
The formula is the average purity of the items used multipled by 0.8, and the addition of a random amount between 1 and the average purity multipied by 0.2.</p>
<br />
<p>Formula = (Average Purity * 0.8) + (Random number from 1 upto (Average Purity * 0.2))</p>
<br />
<p>For example, if you were to use a diamondware of 80% purity, a diamondware of 85% purity, a crystalware of 95% and a Merien stone (100% purity), the formula would be:</p>
<ul><li>Average purity = (80 + 85 + 95 + 100) / 4   = 90% </li>
<li>(90 * 0.8 = 72) and (90 * 0.2 = 18)</li>
<li>Final purity = From 73% to 91%</li>
</ul>
